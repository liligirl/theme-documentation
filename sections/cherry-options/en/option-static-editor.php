			<h3>Static Areas</h3>
			<div class="row-fluid">
				<div class="span6">
					<figure class="img-polaroid">
						<a href="img/cherryframework/cherry-options_static-editor.jpg" class="pretty_image" data-gal="prettyPhoto">
							<img src="img/cherryframework/cherry-options_static-editor.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
						</a>
					</figure>
				</div>
				<div class="span6">
					<p>Use the static area editor to arrange static blocks. You can drag and drop static blocks, remove them or add new ones using the 'Create new static' field below.</p>

					<p>There are several static areas available with self-explanatory names: </p>
					<ul>
						<li>Header top</li>
						<li>Header left</li>
						<li>Header right</li>
						<li>Header bottom</li>
						<li>Slider area</li>
						<li>Footer top</li>
						<li>Footer bottom</li>
					</ul>
					<p>You can use the "Create new static" section to add new static items to static areas. By default a static item will be added to the first static area. You can drag it to the desired static area later. </p>
				</div>
			</div>

			<div class="row-fluid">
				<div class="span6">
					<figure class="img-polaroid">
						<a href="img/cherryframework/cherry-options_static-editor_settings.jpg" class="pretty_image" data-gal="prettyPhoto">
							<img src="img/cherryframework/cherry-options_static-editor_settings.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
						</a>
					</figure>
				</div>
				<div class="span6">
					<p>You can also configure grid settings for each static element and assign custom classes. Column class settings define the static item width according to <a href="http://getbootstrap.com/css/#grid-media-queries">Bootstrap 12 column markup grid</a>: </p>
					<ul>
						<li><b>.col-xs-*</b>: - width for displays with horizontal screen resolution under 768px</li>
						<li><b>.col-sm-*</b>: - width for displays with horizontal screen resolution of 768px or more</li>
						<li><b>.col-md-*</b>: - width for displays with horizontal screen resolution of 992px or more</li>
						<li><b>.col-lg-*</b>: - width for displays with horizontal screen resolution of 1200px or more</li>
					</ul>
				</div>
			</div>

